func (b *Bot) StartWithContext(ctx context.Context) {
	b.logger.Info("ü§ñ –ë–æ—Ç –∑–∞–ø—É—â–µ–Ω (polling)...")
	offset := int64(0)

	for {
		select {
		case <-ctx.Done():
			b.logger.Info("üõë –û—Å—Ç–∞–Ω–æ–≤–∫–∞ polling –ø–æ –∫–æ–Ω—Ç–µ–∫—Å—Ç—É")
			return
		default:
		}

		updates, err := b.safeGetUpdates(ctx, offset)
		if err != nil {
			if ctx.Err() != nil {
				return
			}
			b.logger.Warn("getUpdates error: %w", err)
			b.logger.Warn("getUpdates error, retrying...")
			time.Sleep(1 * time.Second) // –º–æ–∂–Ω–æ —Å–¥–µ–ª–∞—Ç—å —ç–∫—Å–ø–æ–Ω–µ–Ω—Ü–∏–∞–ª—å–Ω–æ–µ backoff
			continue
		}

		for _, u := range updates {
			offset = u.UpdateID + 1
			b.cacheMessage(u)
			go func(u Update) {
				defer func() {
					if r := recover(); r != nil {
						b.logger.Error("–ü–∞–Ω–∏–∫–∞ –≤ handleUpdate: %v", r)
					}
				}()
				b.handleUpdate(u)
			}(u)
		}
	}
}

