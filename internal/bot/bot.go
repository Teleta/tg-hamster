func (b *Bot) handleTimeoutCommand(msg *Message) {
	if msg.From == nil {
		return
	}

	var msgID int64
	if !b.isAdmin(msg.Chat.ID, msg.From.ID) {
		msgID = b.safeSendSilent(msg.Chat.ID, "‚ùå –¢–æ–ª—å–∫–æ –∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä –º–æ–∂–µ—Ç –∑–∞–¥–∞–≤–∞—Ç—å —Ç–∞–π–º–∞—É—Ç")
		time.AfterFunc(5*time.Second, func() {
			b.safeDeleteMessage(msg.Chat.ID, msgID)
		})
		return
	}

	parts := strings.Fields(msg.Text)
	if len(parts) < 2 {
		msgID = b.safeSendSilent(msg.Chat.ID, "‚öôÔ∏è –ò—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ: /timeout <—Å–µ–∫—É–Ω–¥>")
		time.AfterFunc(5*time.Second, func() {
			b.safeDeleteMessage(msg.Chat.ID, msgID)
		})
		return
	}

	timeoutSecVar, err := strconv.Atoi(parts[1])
	if err != nil || timeoutSecVar < 5 || timeoutSecVar > 600 {
		msgID = b.safeSendSilent(msg.Chat.ID, "‚öôÔ∏è –£–∫–∞–∂–∏—Ç–µ –∑–Ω–∞—á–µ–Ω–∏–µ –æ—Ç 5 –¥–æ 600 —Å–µ–∫—É–Ω–¥")
		time.AfterFunc(5*time.Second, func() {
			b.safeDeleteMessage(msg.Chat.ID, msgID)
		})
		return
	}

	b.timeouts.Set(msg.Chat.ID, timeoutSecVar)
	b.timeouts.Save(b.timeoutFile, b.logger)
	msgID = b.safeSendSilent(msg.Chat.ID, fmt.Sprintf("‚úÖ –¢–∞–π–º–∞—É—Ç —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω: %d —Å–µ–∫.", timeoutSecVar))
	time.AfterFunc(5*time.Second, func() {
		b.safeDeleteMessage(msg.Chat.ID, msgID)
	})
}

